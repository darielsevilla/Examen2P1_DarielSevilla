    public String impMat(int[][] mat){
        String temp = "";
        for(int i = 0; i < mat.length; i++){
            for(int j = 0; j < mat[i].length; j++){
                temp += String.format("[%d]", mat[i][j]);
            }
            temp += "\n";
        }
        return temp;
    }
    
    public String impArray(int[] par){
        String resp = "";
        for(int i = 0; i < par.length; i++){
            resp += String.format("[%d]", par[i]);
        }
        resp += "\n";
        return resp;
    }
