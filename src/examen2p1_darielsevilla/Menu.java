    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String check = "";
        do{
            check = JOptionPane.showInputDialog("Ingrese cantidad de filas/columnas de matriz");
        }while(!checkInt(check));
        
        int[][] mat = lectura(Integer.parseInt(check));
        this.mat = mat;
        String temp = "";
        do{
            temp = JOptionPane.showInputDialog("Ingrese numero");
        }while(!checkInt(temp)); 
        
        int num = Integer.parseInt(temp);
        String resp = "Matriz original: \n";
        resp += impMat(this.mat);
        Matrices matriz = new Matrices(num, this.mat);
        
        resp += "Matriz respuesta: \n";
        resp += impMat(matriz.getMatResp());
        JOptionPane.showMessageDialog(null, resp);
        JOptionPane.showMessageDialog(null, "Arreglo ordenado: \n" + impArray(matriz.getArrayResp()));
    }//GEN-LAST:event_jButton1ActionPerformed

